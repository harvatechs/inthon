def _real_fs_read(path: str) -> str:
    with open(path, "r", encoding="utf-8") as fh:
        return fh.read()


def _real_fs_write(path: str, content: str) -> bool:
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(content)
    return True


