# web.*
# ---------------------------------------------------------------------------
def _mock_web_search(query: str, limit: int = 5) -> list[dict]:
    limit = max(0, int(limit))
    words = [w for w in query.split() if w] or ["result"]
    topic = " ".join(words[:6])
    out = []
    for i in range(1, limit + 1):
        slug = _stable_id(f"{query}#{i}")
        out.append(
            {
                "title": f"{topic.title()} — result {i}",
                "url": f"https://mock.inthon.dev/search/{slug}",
                "snippet": (
                    f"Mock result {i} for '{query}'. Deterministic offline "
                    f"content generated by the INTHON mock web.search tool."
                ),
                "rank": i,
            }
        )
    return out


def _real_web_search(query: str, limit: int = 5) -> list[dict]:
    import urllib.request
    import urllib.parse
    import re
    import html

    limit = max(1, int(limit))
    encoded = urllib.parse.quote(query)
    url = f"https://html.duckduckgo.com/html/?q={encoded}"
    req = urllib.request.Request(
        url,
        headers={
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
        },
    )
    try:
        with urllib.request.urlopen(req, timeout=10) as resp:
            raw_html = resp.read().decode("utf-8", errors="replace")

        results = []
        matches = re.findall(
            r'<a[^>]*class="result__url"[^>]*href="([^"]+)"[^>]*>(.*?)</a>.*?'
            r'<a[^>]*class="result__snippet"[^>]*>(.*?)</a>',
            raw_html,
            re.DOTALL,
        )
        if not matches:
            matches = re.findall(
                r'<h2[^>]*class="result__title"[^>]*>\s*<a[^>]*href="([^"]+)"[^>]*>(.*?)</a>.*?'
                r'<a[^>]*class="result__snippet"[^>]*>(.*?)</a>',
                raw_html,
                re.DOTALL,
            )

        for idx, match in enumerate(matches[:limit], 1):
            href, title, snippet = match
            title_clean = html.unescape(re.sub(r"<[^>]+>", "", title)).strip()
            snippet_clean = html.unescape(re.sub(r"<[^>]+>", "", snippet)).strip()
            results.append(
                {
                    "title": title_clean or f"Result {idx}",
                    "url": href,
                    "snippet": snippet_clean,
                    "rank": idx,
                }
            )
        if results:
            return results
    except Exception:
        pass
    return _mock_web_search(query, limit)


def _mock_web_read(url) -> Any:
    if isinstance(url, list):
        return [_mock_web_read(u) for u in url]
    slug = _stable_id(str(url))
    body = (
        f"# Mock page for {url}\n\n"
        f"Document id: {slug}.\n\n"
        + textwrap.dedent(
            """\
            This is deterministic content returned by the INTHON web.read tool.
            Every paragraph of this text is generated from the URL alone.
            """
        )
        * 3
    )
    return body


def _real_web_read(url):  # pragma: no cover - network
    import urllib.request
    import re

    if isinstance(url, list):
        return [_real_web_read(u) for u in url]
    req = urllib.request.Request(str(url), headers={"User-Agent": "inthon/1.0"})
    with urllib.request.urlopen(req, timeout=10) as resp:  # noqa: S310 - gated by policy
        charset = "utf-8"
        headers = resp.headers
        if headers:
            if (
                hasattr(headers, "get_content_charset")
                and headers.get_content_charset()
            ):
                charset = headers.get_content_charset()
            elif isinstance(headers, dict):
                ct = headers.get("Content-Type", "")
                if "charset=" in ct:
                    charset = ct.split("charset=")[-1].split(";")[0].strip()
        data = resp.read()
        if charset.lower() == "utf-8":
            snippet = data[:1024].decode("ascii", errors="ignore")
            m = re.search(
                r'<meta[^>]*charset=["\']?([a-zA-Z0-9_-]+)', snippet, re.IGNORECASE
            )
            if m:
                charset = m.group(1)
            else:
                m2 = re.search(r"charset=([a-zA-Z0-9_-]+)", snippet, re.IGNORECASE)
                if m2:
                    charset = m2.group(1)
        return data.decode(charset, errors="replace")


_web_read_real = _real_web_read


